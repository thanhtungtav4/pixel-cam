#!/usr/bin/env bash
#
# Install a pre-commit hook that runs:
#   - acf-json/validate.php        (catch duplicate ACF field keys)
#   - bin/build-translations.sh    (keep languages/ in sync with source)
#
# Usage:
#   bin/install-pre-commit.sh      # install into .git/hooks/pre-commit
#
# To bypass for a single commit:  git commit --no-verify
#
# @package underscores
set -euo pipefail

here="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
theme="$(cd "$here/.." && pwd)"

# Walk up to find the .git directory (works for both theme-as-repo and
# child-theme-inside-parent-repo layouts).
git_root="$(git -C "$theme" rev-parse --show-toplevel 2>/dev/null || true)"
if [[ -z "$git_root" ]]; then
    echo "ERROR: not inside a git working tree" >&2
    exit 1
fi

hook_path="$git_root/.git/hooks/pre-commit"

# Don't overwrite an existing hook — append our checks after the shebang.
if [[ -f "$hook_path" ]]; then
    echo "WARN: pre-commit hook already exists at $hook_path"
    echo "      Add the following lines manually to run the ACF + i18n checks:"
    echo ""
    echo "      # Pixel Cam ACF + i18n pre-commit"
    echo "      \"$theme/acf-json/validate.php\" || exit 1"
    echo "      \"$theme/bin/build-translations.sh\" || exit 1"
    echo "      git add \"$theme/languages/\""
    exit 0
fi

cat > "$hook_path" <<EOF
#!/usr/bin/env bash
# Pixel Cam — ACF JSON + i18n build pre-commit hook.
# Generated by bin/install-pre-commit.sh — re-run to update.
set -euo pipefail

theme="$theme"

echo "→ acf-json/validate.php"
php "\$theme/acf-json/validate.php"

echo "→ bin/build-translations.sh"
"\$theme/bin/build-translations.sh"

# Auto-stage any updated translation files.
git add "\$theme/languages/" 2>/dev/null || true
EOF

chmod +x "$hook_path"

echo "Installed: $hook_path"
echo "Try it: git commit (a no-op commit will run the checks)"
